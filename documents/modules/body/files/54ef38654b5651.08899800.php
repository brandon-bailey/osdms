<p>To set up additional email accounts:</p>

<p>Go to the Account Editor web site, at&nbsp;<a href="https://security.frontier.com/acctedit" target="_blank"><strong>https://security.frontier.com/acctedit</strong></a>.</p>

<p style="margin-left:38.25pt;">&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Log in by typing your&nbsp;<strong>main</strong>&nbsp;Frontier, FrontierNet, Citlink, Newnorth, Epix, or GVNI email address and your password&nbsp;►&nbsp;Click Login&nbsp;►&nbsp;Additional Email Addresses.</p>

<p style="margin-left:38.25pt;">&middot;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; You will see your current email accounts and the number of accounts you can create at no extra charge. You may also add or remove email accounts.</p>

