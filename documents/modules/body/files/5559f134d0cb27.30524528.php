<h3>Overview</h3>

<p>A port bounce, or bouncing a port, is an important step to FiOS troubleshooting and can resolve a lot of issues such as:</p>

<ul>
	<li>Amber internet light</li>
	<li>Green internet but not browsing</li>
	<li>Speed issues</li>
	<li>Not getting the guide or VOD</li>
</ul>

<p>Bouncing the port will do the following:</p>

<ul>
	<li>Disables the selected port</li>
	<li>Removes provisioning for the selected port</li>
	<li>Rebuilds provisioning for the selected port</li>
	<li>Enables the selected port</li>
</ul>
