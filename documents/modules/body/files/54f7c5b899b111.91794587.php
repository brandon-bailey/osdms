<h4>How do I set my TCP/IP properties to dynamically accept the Frontier DNS servers?</h4>

<div>
<h5>The following are instructions for assigning DNS server addresses dynamically on Windows 2000, XP, Vista, 7 and Mac OS X for both High-Speed Internet and Dial-up users.</h5>

<p>Should you have questions at any time, please contact <a href="/customerservice/2" title="Contact Tech Support">Tech Support</a>.</p>

<p><strong>For Windows 2000:</strong></p>

<p>Note: This MIGHT require your Windows CD.</p>

<ul>
	<li>From your desktop, click Start Settings Control Panel.</li>
	<li>When the Control Panel menu appears, double-click the Network and Dial-up Connections icon.</li>
	<li>If you use <strong>High-Speed Internet</strong>, right click the LAN connection click Properties.
	<ul>
		<li>Click the General tab, and make sure TCP/IP is checked.</li>
		<li>Click the TCP/IP entry for your Ethernet adapter (or USB adapter, depending how your DSL connection is installed).</li>
		<li>Click Properties to continue.</li>
		<li>Click the General tab, and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK to close the TCP/IP window.</li>
		<li>Click OK to close the LAN Properties window.</li>
		<li>Windows may prompt you to restart your computer. If it does Click Yes.</li>
		<li>If Windows doesn&#39;t prompt you to restart, close all windows and reboot your computer Click Windows Start button Select Shut Down Click Restart.</li>
	</ul>
	</li>
	<li>If you use <strong>Dial-up Internet</strong>, right-click your dial-up connection Click Properties.
	<ul>
		<li>Click Networking tab and make sure TCP/IP is checked.</li>
		<li>Select the TCP/IP entry.</li>
		<li>Click Properties to continue Click General tab and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK to close the TCP/IP window.</li>
		<li>Click OK to close the dial-up connection Properties window.</li>
		<li>Windows may prompt you to reboot your computer. If it does Click Yes.</li>
		<li>If Windows doesn&#39;t prompt you to restart, close all windows and reboot your computer Click Windows Start button Select Shut Down Click Restart.</li>
	</ul>
	</li>
</ul>

<p><strong>For Windows XP:</strong></p>

<p>Note: This MIGHT require your Windows CD.</p>

<ul>
	<li>From your desktop, click Start Control Panel.</li>
	<li>In the Control Panel, double-click the Network Connections icon.</li>
	<li>If you use <strong>High-Speed Internet</strong>, right-click the LAN connection Click Properties.
	<ul>
		<li>Click the General tab and make sure TCP/IP is checked.</li>
		<li>Click the TCP/IP entry for your Ethernet adapter (or USB adapter, depending how your DSL connection is installed).</li>
		<li>Click Properties to continue.</li>
		<li>Click the General tab and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK to close the TCP/IP window.</li>
		<li>Click OK to close the LAN Properties window. Windows may prompt you to reboot your computer. If it does, click Yes. If it doesn&#39;t, close all windows and reboot your computer by clicking the Windows Start button, selecting Shut Down and clicking Restart.</li>
	</ul>
	</li>
	<li>If you use <strong>Dial-up Internet</strong>, right click your dial-up connection click Properties.
	<ul>
		<li>Click the Networking tab and make sure TCP/IP is checked.</li>
		<li>Select the TCP/IP entry and click Properties to continue.</li>
		<li>Click the General tab and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK to close the TCP/IP window.</li>
		<li>Click OK to close the Dial-up Properties window. Windows may prompt you to reboot your computer. If it does, click Yes. If it doesn&#39;t, close all windows and reboot your computer by clicking the Windows Start button, selecting Shut Down and clicking Restart.</li>
	</ul>
	</li>
</ul>

<p><strong>For Windows Vista:</strong></p>

<p>Note: This MIGHT require your Windows CD.</p>

<ul>
	<li>From your desktop, click Windows Start Control Panel.</li>
	<li>In the Control Panel, double-click the Network and Sharing Center icon.</li>
	<li>If you use <strong>High-Speed Internet</strong> Select the View Status link for the LAN (Local Area Network) connection&nbsp;Click Properties.
	<ul>
		<li>If you see a User Account Control window Click Continue.</li>
		<li>Click the General tab and make sure Internet Protocol version 4 (TCP/IPv4) is checked.</li>
		<li>Select the TCP/IP entry for your Ethernet adapter (or USB adapter, depending how your DSL connection is installed) Click Properties.</li>
		<li>Click the General tab and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK to close the TCP/IP window.</li>
		<li>Click Close to close the LAN Properties window and the Local Area Connection Status window. Windows may prompt you to restart your computer. If it does Click Yes.</li>
		<li>If Windows doesn&#39;t prompt you to restart, close all windows and reboot your computer Click Windows Start button Select Restart.</li>
	</ul>
	</li>
	<li>If you use <strong>Dial-up Internet</strong>, while you are in the Network and Sharing Center window Click Manage Network Connections link on the left-hand side of the window.
	<ul>
		<li>Right-click on your dial-up connection Select Properties.</li>
		<li>If you receive a User Account Control window Click Continue.</li>
		<li>Click the Networking tab and make sure the Internet Protocol version 4 (TCP/IPv4) is checked.</li>
		<li>Click the TCP/IP entry Click Properties.</li>
		<li>Click the General tab and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK to close the TCP/IP window.</li>
		<li>Click OK to close the Dial-up Properties window. Windows may prompt you to restart your computer. If it does Click Yes.</li>
		<li>If Windows doesn&#39;t prompt you to restart, close all windows and reboot your computer Click Windows Start button&nbsp;Select Restart.</li>
	</ul>
	</li>
</ul>

<p><strong>For Windows 7:</strong></p>

<p>Note: This MIGHT require your Windows CD.</p>

<ul>
	<li>From your desktop Click Windows Start Click Control Panel.</li>
	<li>In the Control Panel Click View Network Status and Tasks Click Change Adapter Settings.</li>
	<li>If you use <strong>High-Speed Internet</strong> Right-click your local area connection&nbsp;Click Properties:
	<ul>
		<li>If you see a User Account Control window Click Continue.</li>
		<li>Make sure Internet Protocol version 4 (TCP/IPv4) is checked.</li>
		<li>Select the TCP/IP entry for your Ethernet adapter (or USB adapter, depending how your DSL connection is installed) Click Properties.</li>
		<li>Click the General tab and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK to close the TCP/IP window.</li>
		<li>Click Close to close the LAN Properties window and the Local Area Connection Status window. Windows may prompt you to restart your computer. If it does Click Yes.</li>
		<li>If Windows doesn&#39;t prompt you to restart, close all windows and reboot your computer Click Windows Start button Select Restart.</li>
	</ul>
	</li>
	<li>If you use <strong>Dial-up Internet</strong> Click Change Adapter settings:
	<ul>
		<li>Right-click your dial-up connection Select Properties.</li>
		<li>If you receive a User Account Control window Click Continue.</li>
		<li>Click the Networking tab and make sure the Internet Protocol version 4 (TCP/IPv4) is checked.</li>
		<li>Click the TCP/IP entry Click Properties.</li>
		<li>Click the General tab and make sure Obtain DNS Server Address Automatically is selected.</li>
		<li>Click OK.</li>
		<li>Click OK to close the Dial-up Properties window. Windows may prompt you to restart your computer. If it does Click Yes.</li>
		<li>If Windows doesn&#39;t prompt you to restart, close all windows and reboot your computer Click Windows Start button Select Restart.</li>
	</ul>
	</li>
</ul>

<p><strong>For Mac OS X:</strong></p>

<p>If you use either <strong>High-Speed Internet</strong> or <strong>Dial-up Internet</strong> Click the Apple System Preferences Network. On the TCP/IP tab, make sure that no Domain Name Servers are entered. If they are, remove them Click Apply Now.</p>
